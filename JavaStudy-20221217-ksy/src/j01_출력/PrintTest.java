package j01_출력;

public class PrintTest {

	public static void main(String[] args) {
		System.out.println("Hello, Java!");
		
		System.out.print("김소유");
		System.out.println("------");
		
		System.out.print("주말수업");
		System.out.println("--");
		
		// 주석 : 프로그램에 영향을 주지 않음
//		ctrl+f11 실행단축기
//		ctrl+ / 주석단축기
//		println은 줄바꿈
//		print는 줄바꿈 안함
		
		System.out.print("Hello, \nJava\n");
		System.out.println("김소유");
		
		//줄바꿈	: 원하는 위치에서 \n	뒤에다가 붙이면  
//		그 다음에 출력하는 것도 줄바꿈이 된다
		
		int level ; 
		level = 10 ; 
		System.out.println(level);
		level=11;
		System.out.println(level);
		
		
		
		
	}
	
	

}
